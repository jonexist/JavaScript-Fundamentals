//Conditions

/* Using (if/else) */
let name = 'Jonathan';

if (typeof name =='string') {
  console.log('YES');
} else {
  console.log('NO');
}
//end

/* Using switch */
let age = 21;

switch (age) {
  case 21:
    console.log('Twenty One'); break;
  case 20:
    console.log('Twenty'); break;
  default:
    console.log('None'); 
}